#get data type
#menampilkan tipe data

nama = "Refina" #string, "" or ''
print(nama)
print(type(nama))

umur = 19 #integer (bilangan bulat positif & negatif)
print(umur)
print(type(umur))
angka = -198
print(angka)
print(type(angka))

nomor = 9.555 #float (tipe data untuk bilangan decimal (ada komanya) negatif & positif)
print(nomor)
print(type(nomor))

imaginer = 6 +4j #complex (ada bilangan imajiner (j))
print(imaginer)
print(type(imaginer))

fruits = ["aple", "banana", "grape"] #tipe data list (berisi data yang banyak menggunakan [])
print(fruits)
print(type(fruits))


legal = True #boolean, just True or False
print(legal)
print(type(legal))
ilegal = False #boolean
print(ilegal)
print(type(ilegal))
