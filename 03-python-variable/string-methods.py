nama = "refina alhazen"

print(nama.capitalize())#membuat huruf awal menjadi kapital
#Refina

print(nama.upper())#membuat semua huruf menjadi kapital
#REFINA ALHAZEN

print(nama.lower())#membuat semua huruf menjadi kecil
#refina alhazen

print(nama.split())#membagi isi variable
#['refina', 'alhazen']