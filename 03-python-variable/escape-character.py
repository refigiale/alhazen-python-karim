#MENAMPILKAN KARAKTER ILLEGA/YG TIDAK DIPERBOLEHKAN MENGGUNAKAN (\)
print("Hello world")
#Hello World

print("\"Hello World\"")
#"Hello World"

print("\'Hello World\'")
#'Hello World'


#MENAMPILKAN/MEMBUAT BARIS BARU (\n) NEWLINE
print("Hello\nWorld")
#Hello
#World

#MENGHAPUS (\b) BACKSPACE
print("H\belloWorld") #menghapus huruf di depannya
#elloWorld

#MEMBERIKAN SPASI (\t) for Tab
print("He\tllo World")